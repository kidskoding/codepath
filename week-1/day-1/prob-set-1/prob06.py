def doubled(hunny_jars: list[int]) -> list[int]:
    # 1. Multiply the list by 2
    # 2. Return the resulting list. By multiplying the list by 2, it doubles every element in the list!

    # Time: O(n) because it takes up to n times to go through the list and multiply each element by 2
    # Space: O(1) because there is no additional space that takes up when multiplying each element by 2 in the list

    return hunny_jars * 2
